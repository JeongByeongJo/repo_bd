package com.byeongjo.fapp.dates;

public class DatesDto {
	
	private String seq;
	private String isMain;
	private String datesCategory;
	private String dates;
	private String isLunar;
	private String addressBook_seq;
//	-----
	public String getSeq() {
		return seq;
	}
	public void setSeq(String seq) {
		this.seq = seq;
	}
	public String getIsMain() {
		return isMain;
	}
	public void setIsMain(String isMain) {
		this.isMain = isMain;
	}
	public String getDatesCategory() {
		return datesCategory;
	}
	public void setDatesCategory(String datesCategory) {
		this.datesCategory = datesCategory;
	}
	public String getDates() {
		return dates;
	}
	public void setDates(String dates) {
		this.dates = dates;
	}
	public String getIsLunar() {
		return isLunar;
	}
	public void setIsLunar(String isLunar) {
		this.isLunar = isLunar;
	}
	public String getAddressBook_seq() {
		return addressBook_seq;
	}
	public void setAddressBook_seq(String addressBook_seq) {
		this.addressBook_seq = addressBook_seq;
	}
	

}
